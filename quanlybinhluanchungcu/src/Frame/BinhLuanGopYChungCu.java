    public void LayDuLieu(){
        DBCursor cursor = user.find();
        while(cursor.hasNext()){
            DBObject doc = cursor.next();
            Object _id = doc.get("_id");
            String name = doc.get("name").toString();
            Vector v = new Vector();
            v.add(_id); v.add(name);
            tblData.add(v);
            tblUser.setModel(new DefaultTableModel(tblData, tblTitle));
        }
    }

    public void DuaDuLieuVaoCombobox(){
        DBCursor cursor = title.find();
        while(cursor.hasNext()){
            DBObject doc = cursor.next();
            String nametitle = doc.get("nametitle").toString().trim();
            cbboxTieuDe.addItem(nametitle);
        }
    }
    
