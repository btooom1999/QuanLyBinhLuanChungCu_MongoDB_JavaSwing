    private void btnThemUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemUserActionPerformed
        if(txtTaiKhoan.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên tài khoản để thêm user", "Thông báo", 2);
            return;
        }
        
        BasicDBObject doc = new BasicDBObject();
        doc.put("name", txtTaiKhoan.getText().trim());
        
        user.insert(doc);
        txtTaiKhoan.setText("");
        XoaDuLieu();
        LayDuLieu();
    }//GEN-LAST:event_btnThemUserActionPerformed

    private void btnThemTieuDeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemTieuDeActionPerformed
        if(txtTieuDe.getText().equals("") || txtTaiKhoan.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên tài khoản và tiêu đề để thêm bài bình luận", "Thông báo", 2);
            return;
        }
        
        BasicDBObject doc = new BasicDBObject();
        doc.put("nametitle", txtTieuDe.getText().trim());
        doc.put("name", txtTaiKhoan.getText().trim());
        title.insert(doc);
        cbboxTieuDe.addItem(txtTieuDe.getText().trim());
        txtTaiKhoan.setText("");
        txtTieuDe.setText("");
    }//GEN-LAST:event_btnThemTieuDeActionPerformed

    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        int i = tblUser.getSelectedRow();
        txtTaiKhoan.setText(tblUser.getValueAt(i, 1).toString().trim());
        txtTaiKhoan.setEnabled(false);
        btnThemUser.setEnabled(false);
    }//GEN-LAST:event_tblUserMouseClicked
    
    private void btnThemBinhLuanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemBinhLuanActionPerformed
        if(cbboxTieuDe.getSelectedIndex() < 0|| txtTaiKhoan.getText().equals("") || txtBinhLuan.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ tên tài khoản, bình luận và chọn tiêu đề để thêm bài bình luận", "Thông báo", 2);
            return;
        }
        BasicDBObject doc = new BasicDBObject();
        doc.put("nametitle", cbboxTieuDe.getSelectedItem().toString().trim());
        doc.put("name", txtTaiKhoan.getText().trim());
        doc.put("comment", txtBinhLuan.getText().trim());
        comment.insert(doc);
        String str = "Name: " + txtTaiKhoan.getText().trim() + "\nComment: " + txtComment.getText().trim() + "\n\n";
        txtComment.setText(txtComment.getText() + "Name: " + txtTaiKhoan.getText().trim() + "\nComment: " + txtBinhLuan.getText().trim() + "\n\n");
        txtTaiKhoan.setText("");
        txtBinhLuan.setText("");
    }//GEN-LAST:event_btnThemBinhLuanActionPerformed

    public String LayCommentTheoTieuDe(){
        String str = "";
        BasicDBObject Query = new BasicDBObject();
        Query.put("nametitle", cbboxTieuDe.getSelectedItem().toString().trim());
        DBCursor cursor = comment.find(Query);
        while(cursor.hasNext()){
            DBObject doc = cursor.next();
            String name = doc.get("name").toString();
            String comment = doc.get("comment").toString();
            str += "Name: " + name + "\nComment: " + comment + "\n\n";
        }
        return str;
    }
    
